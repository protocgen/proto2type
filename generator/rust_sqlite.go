// generateRustSqlite generates a Rust SQLite storage type file for a proto file.
func generateRustSqlite(gen *protogen.Plugin, file *protogen.File, opts *Options) error {
	filename := outputFilename(file.GeneratedFilenamePrefix, "_sqlite.type.rs")

	g := gen.NewGeneratedFile(filename, "")

	// Build IR for import scanning.
	df := BuildDomainFile(file, opts)

	// Header
	g.P("// Code generated by proto2type. DO NOT EDIT.")
	g.P("// proto2type v", Version)
	g.P("// source: ", file.Desc.Path())
	g.P("// backend: sqlite")
	g.P()

	// Scan IR for needed imports.
	needsChrono := irNeedsChronoSqlite(df.Messages)

	// Emit use statements
	g.P("use super::*;")
	g.P("use serde::{Deserialize, Serialize};")
	if needsChrono {
		g.P("use chrono::{DateTime, Utc};")
	}
	g.P()

	// Emit ConversionError type (P1-9)
	generateRustConversionError(g, needsChrono)

	// Generate helper functions if chrono is needed
	if needsChrono {
		generateRustEpochMsHelpers(g)
	}

	// Build a lookup from FullName -> *protogen.Message for converter generation.
	protoMsgMap := buildProtoMessageMap(file.Messages)

	for _, dm := range df.Messages {
		msg := protoMsgMap[dm.FullName]
		generateRustSqliteMessage(g, dm, msg, protoMsgMap, opts)
	}

	return nil
}

// irNeedsChronoSqlite returns true if any non-skipped, non-docID field is a Timestamp.
func irNeedsChronoSqlite(msgs []*DomainMessage) bool {
	for _, m := range msgs {
		if m.Skip {
			continue
		}
		for _, f := range m.Fields {
			if f.DocID {
				continue
			}
			if f.Kind == FieldKindTimestamp {
				return true
			}
		}
		if irNeedsChronoSqlite(m.NestedMessages) {
			return true
		}
	}
	return false
}

// generateRustConversionError emits a ConversionError enum type for SQLite conversion (P1-9).
func generateRustConversionError(g *protogen.GeneratedFile, needsChrono bool) {
	g.P("#[derive(Debug)]")
	g.P("pub enum ConversionError {")
	g.P("    Json(serde_json::Error),")
	if needsChrono {
		g.P("    InvalidTimestamp(i64),")
	}
	g.P("    InvalidEnumValue(i32),")
	g.P("    Overflow,")
	g.P("}")
	g.P()
	g.P("impl std::fmt::Display for ConversionError {")
	g.P("    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {")
	g.P("        match self {")
	g.P(`            Self::Json(e) => write!(f, "json: {e}"),`)
	if needsChrono {
		g.P(`            Self::InvalidTimestamp(ms) => write!(f, "invalid timestamp: {ms}ms"),`)
	}
	g.P(`            Self::InvalidEnumValue(v) => write!(f, "invalid enum value: {v}"),`)
	g.P(`            Self::Overflow => write!(f, "integer overflow during conversion"),`)
	g.P("        }")
	g.P("    }")
	g.P("}")
	g.P()
	g.P("impl std::error::Error for ConversionError {}")
	g.P()
	g.P("impl From<serde_json::Error> for ConversionError {")
	g.P("    fn from(e: serde_json::Error) -> Self {")
	g.P("        Self::Json(e)")
	g.P("    }")
	g.P("}")
	g.P()
}

// generateRustEpochMsHelpers generates epoch_ms conversion functions.
func generateRustEpochMsHelpers(g *protogen.GeneratedFile) {
	g.P("/// Converts epoch milliseconds to a chrono DateTime<Utc>.")
	g.P("#[allow(dead_code)]")
	g.P("fn epoch_ms_to_datetime(ms: i64) -> Result<DateTime<Utc>, ConversionError> {")
	g.P("    DateTime::from_timestamp_millis(ms).ok_or(ConversionError::InvalidTimestamp(ms))")
	g.P("}")
	g.P()
	g.P("/// Converts a chrono DateTime<Utc> to epoch milliseconds.")
	g.P("#[allow(dead_code)]")
	g.P("fn datetime_to_epoch_ms(dt: &DateTime<Utc>) -> i64 {")
	g.P("    dt.timestamp_millis()")
	g.P("}")
	g.P()
}

// generateRustSqliteMessage generates a SQLite Row struct and conversions for a message.
func generateRustSqliteMessage(g *protogen.GeneratedFile, dm *DomainMessage, msg *protogen.Message, protoMsgMap map[string]*protogen.Message, opts *Options) {
	if dm.Skip {
		return
	}

	rowName := dm.Name + "Row"
	domainName := dm.Name

	// Find document_id field name from IR
	var docIDFieldName string
	var docIDField *protogen.Field // kept for converter signature compat
	for _, f := range dm.Fields {
		if f.DocID {
			docIDFieldName = f.Name
			break
		}
	}
	if docIDFieldName != "" && msg != nil {
		for _, field := range msg.Fields {
			if isDocumentID(field) {
				docIDField = field
				break
			}
		}
	}

	// Row struct (from IR)
	g.P("/// SQLite storage representation of ", dm.FullName, ".")
	g.P("#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]")
	g.P("pub struct ", rowName, " {")

	for _, f := range dm.Fields {
		if f.IsOneof {
			// Oneof stored as JSON string
			rustFieldName := escapeRustKeyword(toSnakeCase(f.Name))
			g.P("    pub ", rustFieldName, ": Option<String>,")
			continue
		}
		if f.DocID {
			continue // Document ID passed separately
		}

		rustFieldName := escapeRustKeyword(toSnakeCase(f.Name))
		fieldType := rustSqliteFieldTypeFromIR(f)

		g.P("    pub ", rustFieldName, ": ", fieldType, ",")
	}

	g.P("}")
	g.P()

	// impl block
	g.P("impl ", rowName, " {")

	// from_row: construct from rusqlite::Row (from IR)
	g.P("    /// Constructs a ", rowName, " from a rusqlite::Row using named column access.")
	g.P("    pub fn from_row(row: &rusqlite::Row) -> rusqlite::Result<Self> {")
	g.P("        Ok(Self {")

	for _, f := range dm.Fields {
		if f.IsOneof {
			rustFieldName := escapeRustKeyword(toSnakeCase(f.Name))
			g.P("            ", rustFieldName, ": row.get(\"", f.Name, "\")?,")
			continue
		}
		if f.DocID {
			continue
		}

		rustFieldName := escapeRustKeyword(toSnakeCase(f.Name))
		g.P("            ", rustFieldName, ": row.get(\"", f.Name, "\")?,")
	}

	g.P("        })")
	g.P("    }")
	g.P()

	// to_domain: convert Row to domain type
	g.P("    /// Converts this row to the domain type.")
	if docIDField != nil {
		docIDRustName := escapeRustKeyword(toSnakeCase(string(docIDField.Desc.Name())))
		g.P("    pub fn to_domain(&self, ", docIDRustName, ": String) -> Result<", domainName, ", ConversionError> {")
	} else {
		g.P("    pub fn to_domain(&self) -> Result<", domainName, ", ConversionError> {")
	}
	g.P("        Ok(", domainName, " {")

	seenOneofs := map[string]bool{}
	for _, field := range msg.Fields {
		if field.Oneof != nil && !field.Oneof.Desc.IsSynthetic() {
			oneofName := string(field.Oneof.Desc.Name())
			if !seenOneofs[oneofName] {
				seenOneofs[oneofName] = true
				rustFieldName := escapeRustKeyword(toSnakeCase(oneofName))
				g.P("            ", rustFieldName, ": match &self.", rustFieldName, " { Some(s) => Some(serde_json::from_str(s)?), None => None },")
			}
			continue
		}
		if isFieldSkipped(field) {
			continue
		}

		protoName := string(field.Desc.Name())
		rustFieldName := escapeRustKeyword(toSnakeCase(protoName))

		if isDocumentID(field) {
			// Inject from parameter
			g.P("            ", rustFieldName, ",")
			continue
		}

		conversion := rustSqliteToDomainConversion(field, rustFieldName, opts)
		g.P("            ", rustFieldName, ": ", conversion, ",")
	}

	g.P("        })")
	g.P("    }")
	g.P()

	// into_domain: consuming variant (P1-10)
	g.P("    /// Converts this row into the domain type, consuming self.")
	if docIDField != nil {
		docIDRustName := escapeRustKeyword(toSnakeCase(string(docIDField.Desc.Name())))
		g.P("    pub fn into_domain(self, ", docIDRustName, ": String) -> Result<", domainName, ", ConversionError> {")
	} else {
		g.P("    pub fn into_domain(self) -> Result<", domainName, ", ConversionError> {")
	}
	g.P("        Ok(", domainName, " {")

	seenOneofs = map[string]bool{}
	for _, field := range msg.Fields {
		if field.Oneof != nil && !field.Oneof.Desc.IsSynthetic() {
			oneofName := string(field.Oneof.Desc.Name())
			if !seenOneofs[oneofName] {
				seenOneofs[oneofName] = true
				rustFieldName := escapeRustKeyword(toSnakeCase(oneofName))
				g.P("            ", rustFieldName, ": match self.", rustFieldName, " { Some(s) => Some(serde_json::from_str(&s)?), None => None },")
			}
			continue
		}
		if isFieldSkipped(field) {
			continue
		}

		protoName := string(field.Desc.Name())
		rustFieldName := escapeRustKeyword(toSnakeCase(protoName))

		if isDocumentID(field) {
			// Inject from parameter
			g.P("            ", rustFieldName, ",")
			continue
		}

		conversion := rustSqliteIntoDomainConversion(field, rustFieldName, opts)
		g.P("            ", rustFieldName, ": ", conversion, ",")
	}

	g.P("        })")
	g.P("    }")
	g.P()

	// from_domain: convert domain type to Row
	g.P("    /// Constructs a row from the domain type.")
	g.P("    pub fn from_domain(d: &", domainName, ") -> Result<Self, ConversionError> {")
	g.P("        Ok(Self {")

	seenOneofs = map[string]bool{}
	for _, field := range msg.Fields {
		if field.Oneof != nil && !field.Oneof.Desc.IsSynthetic() {
			oneofName := string(field.Oneof.Desc.Name())
			if !seenOneofs[oneofName] {
				seenOneofs[oneofName] = true
				rustFieldName := escapeRustKeyword(toSnakeCase(oneofName))
				g.P("            ", rustFieldName, ": match &d.", rustFieldName, " { Some(v) => Some(serde_json::to_string(v)?), None => None },")
			}
			continue
		}
		if isFieldSkipped(field) {
			continue
		}
		if isDocumentID(field) {
			continue
		}

		protoName := string(field.Desc.Name())
		rustFieldName := escapeRustKeyword(toSnakeCase(protoName))

		conversion := rustSqliteFromDomainConversion(field, rustFieldName, opts)
		g.P("            ", rustFieldName, ": ", conversion, ",")
	}

	g.P("        })")
	g.P("    }")

	g.P("}")
	g.P()

	// TryFrom impls
	// TryFrom<&DomainType> for RowType (always generated)
	g.P("impl TryFrom<&", domainName, "> for ", rowName, " {")
	g.P("    type Error = ConversionError;")
	g.P("    fn try_from(d: &", domainName, ") -> Result<Self, Self::Error> {")
	g.P("        Self::from_domain(d)")
	g.P("    }")
	g.P("}")
	g.P()

	// TryFrom<RowType> for DomainType (only when no document_id)
	if docIDField == nil {
		g.P("impl TryFrom<", rowName, "> for ", domainName, " {")
		g.P("    type Error = ConversionError;")
		g.P("    fn try_from(row: ", rowName, ") -> Result<Self, Self::Error> {")
		g.P("        row.into_domain()")
		g.P("    }")
		g.P("}")
		g.P()
	}

	// Nested messages
	for _, nested := range dm.NestedMessages {
		nestedMsg := protoMsgMap[nested.FullName]
		generateRustSqliteMessage(g, nested, nestedMsg, protoMsgMap, opts)
	}
}

// rustSqliteFieldType returns the SQLite-appropriate Rust type for a field.
// TODO: proto3 optional handling for Timestamp, Duration, and Enum fields.
// Optional Timestamp should be Option<i64>, optional Duration should be Option<i64>,
// optional Enum should be Option<i32>. The from_row, to_domain, into_domain, and
// from_domain conversion methods also need corresponding updates to handle Option.
func rustSqliteFieldType(field *protogen.Field, opts *Options) string {
	// Timestamps -> i64 (epoch_ms)
	if isWellKnownTimestamp(field) {
		if field.Desc.HasOptionalKeyword() {
			return "Option<i64>"
		}
		return "i64"
	}
	// Durations -> i64 (milliseconds)
	if isWellKnownDuration(field) {
		if field.Desc.HasOptionalKeyword() {
			return "Option<i64>"
		}
		return "i64"
	}

	// Wrapper types -> Option<T> with underlying SQLite type
	if isWellKnownWrapper(field) {
		return rustSqliteWrapperType(field)
	}

	// WKT JSON types -> String (JSON serialized)
	if isWellKnownStruct(field) || isWellKnownValue(field) || isWellKnownListValue(field) || isWellKnownFieldMask(field) {
		return "String"
	}

	// Empty -> skip (unit type has no storage representation, but if present use String)
	if isWellKnownEmpty(field) {
		return "String"
	}

	// Any -> String (JSON)
	if isWellKnownAny(field) {
		// TODO: google.protobuf.Any is too complex for auto-mapping
		return "String"
	}

	// Nested messages -> Option<String> (JSON serialized, NULL for absent) (P1-12)
	if field.Desc.Kind() == protoreflect.MessageKind && !field.Desc.IsList() && !field.Desc.IsMap() {
		return "Option<String>"
	}

	// Repeated fields -> String (JSON serialized)
	if field.Desc.IsList() {
		return "String"
	}

	// Map fields -> String (JSON serialized)
	if field.Desc.IsMap() {
		return "String"
	}

	// Enum types -> i32 in the database
	if field.Desc.Kind() == protoreflect.EnumKind {
		if isEnumAsString(field, opts) {
			if field.Desc.HasOptionalKeyword() {
				return "Option<String>"
			}
			return "String"
		}
		if field.Desc.HasOptionalKeyword() {
			return "Option<i32>"
		}
		return "i32"
	}

	// Optional scalars
	if field.Desc.HasOptionalKeyword() {
		return "Option<" + rustSqliteScalarType(field.Desc.Kind()) + ">"
	}

	return rustSqliteScalarType(field.Desc.Kind())
}

// rustSqliteFieldTypeFromIR returns the SQLite-appropriate Rust type for a field using the IR.
func rustSqliteFieldTypeFromIR(f *DomainField) string {
	switch f.Kind {
	case FieldKindTimestamp, FieldKindDuration:
		if f.Optional {
			return "Option<i64>"
		}
		return "i64"
	case FieldKindStruct, FieldKindValue, FieldKindListValue, FieldKindFieldMask, FieldKindEmpty, FieldKindAny:
		return "String" // JSON serialized
	case FieldKindMessage:
		if f.Repeated || f.IsMap {
			return "String" // JSON serialized
		}
		return "Option<String>" // JSON serialized, NULL for absent
	case FieldKindEnum:
		if f.EnumAsString {
			if f.Optional {
				return "Option<String>"
			}
			return "String"
		}
		if f.Optional {
			return "Option<i32>"
		}
		return "i32"
	case FieldKindScalar:
		if f.Optional {
			return "Option<" + rustSqliteScalarType(f.ScalarKind) + ">"
		}
		if f.Repeated || f.IsMap {
			return "String" // JSON serialized
		}
		return rustSqliteScalarType(f.ScalarKind)
	}

	// Wrapper types -> Option<T> with underlying SQLite type
	if f.Kind.IsWrapper() {
		return rustSqliteWrapperTypeFromIR(f.Kind)
	}

	return rustSqliteScalarType(f.ScalarKind)
}

// rustSqliteWrapperTypeFromIR returns the SQLite Option type for a well-known wrapper from IR.
func rustSqliteWrapperTypeFromIR(kind FieldKind) string {
	switch kind {
	case FieldKindWrapperString:
		return "Option<String>"
	case FieldKindWrapperBool:
		return "Option<bool>"
	case FieldKindWrapperInt32:
		return "Option<i32>"
	case FieldKindWrapperInt64:
		return "Option<i64>"
	case FieldKindWrapperUInt32:
		return "Option<u32>"
	case FieldKindWrapperUInt64:
		return "Option<u64>"
	case FieldKindWrapperFloat:
		return "Option<f32>"
	case FieldKindWrapperDouble:
		return "Option<f64>"
	case FieldKindWrapperBytes:
		return "Option<Vec<u8>>"
	default:
		return "Option<String>"
	}
}

// rustSqliteScalarType returns the SQLite-compatible Rust type for a scalar kind.
func rustSqliteScalarType(kind protoreflect.Kind) string {
	switch kind {
	case protoreflect.BoolKind:
		return "bool"
	case protoreflect.Int32Kind, protoreflect.Sint32Kind, protoreflect.Sfixed32Kind:
		return "i32"
	case protoreflect.Int64Kind, protoreflect.Sint64Kind, protoreflect.Sfixed64Kind:
		return "i64"
	case protoreflect.Uint32Kind, protoreflect.Fixed32Kind:
		return "i64" // SQLite has no unsigned; i64 is safe for u32 range
	case protoreflect.Uint64Kind, protoreflect.Fixed64Kind:
		return "i64" // SQLite has no unsigned; NOTE: u64 > i64::MAX will overflow
	case protoreflect.FloatKind:
		return "f32"
	case protoreflect.DoubleKind:
		return "f64"
	case protoreflect.StringKind:
		return "String"
	case protoreflect.BytesKind:
		return "Vec<u8>"
	default:
		return "String"
	}
}

