package generator

import (
	"fmt"
	"strings"

	"google.golang.org/protobuf/compiler/protogen"
	"google.golang.org/protobuf/reflect/protoreflect"
)

// generateRustSqlite generates a Rust SQLite storage type file for a proto file.
func generateRustSqlite(gen *protogen.Plugin, file *protogen.File, opts *Options) error {
	filename := outputFilename(file.GeneratedFilenamePrefix, "_sqlite.type.rs")

	g := gen.NewGeneratedFile(filename, "")

	// Header
	g.P("// Code generated by proto2type. DO NOT EDIT.")
	g.P("// source: ", file.Desc.Path())
	g.P("// backend: sqlite")
	g.P()

	// Scan for needed imports
	needsChrono := false
	needsSerdeJson := false

	for _, msg := range file.Messages {
		if isMessageSkipped(msg) {
			continue
		}
		scanRustSqliteImports(msg, &needsChrono, &needsSerdeJson)
	}

	// Emit use statements
	g.P("use serde::{Deserialize, Serialize};")
	if needsChrono {
		g.P("use chrono::{DateTime, Utc};")
	}
	if needsSerdeJson {
		// serde_json needed for nested messages serialized as JSON strings
		_ = needsSerdeJson
	}
	g.P()

	// Generate helper functions if chrono is needed
	if needsChrono {
		generateRustEpochMsHelpers(g)
	}

	for _, msg := range file.Messages {
		generateRustSqliteMessage(g, msg, opts)
	}

	return nil
}

// scanRustSqliteImports scans a message for SQLite-specific import needs.
func scanRustSqliteImports(msg *protogen.Message, needsChrono, needsSerdeJson *bool) {
	if isMessageSkipped(msg) {
		return
	}
	for _, field := range msg.Fields {
		if field.Oneof != nil && !field.Oneof.Desc.IsSynthetic() {
			continue
		}
		if isFieldSkipped(field) {
			continue
		}
		if isDocumentID(field) {
			continue
		}
		if isWellKnownTimestamp(field) || isWellKnownDuration(field) {
			*needsChrono = true
		}
		if isNestedMessage(field) {
			*needsSerdeJson = true
		}
		if field.Desc.IsList() && field.Desc.Kind() == protoreflect.MessageKind {
			*needsSerdeJson = true
		}
	}
}

// generateRustEpochMsHelpers generates epoch_ms conversion functions.
func generateRustEpochMsHelpers(g *protogen.GeneratedFile) {
	g.P("/// Converts epoch milliseconds to a chrono DateTime<Utc>.")
	g.P("fn epoch_ms_to_datetime(ms: i64) -> DateTime<Utc> {")
	g.P("    DateTime::from_timestamp_millis(ms).unwrap_or_default()")
	g.P("}")
	g.P()
	g.P("/// Converts a chrono DateTime<Utc> to epoch milliseconds.")
	g.P("fn datetime_to_epoch_ms(dt: &DateTime<Utc>) -> i64 {")
	g.P("    dt.timestamp_millis()")
	g.P("}")
	g.P()
}

// generateRustSqliteMessage generates a SQLite Row struct and conversions for a message.
func generateRustSqliteMessage(g *protogen.GeneratedFile, msg *protogen.Message, opts *Options) {
	if isMessageSkipped(msg) {
		return
	}

	name := toPascalCase(string(msg.Desc.Name()))
	rowName := name + "Row"
	domainName := name

	// Find document_id field
	var docIDField *protogen.Field
	for _, field := range msg.Fields {
		if isDocumentID(field) {
			docIDField = field
			break
		}
	}

	// Row struct
	g.P("/// SQLite storage representation of ", msg.Desc.FullName(), ".")
	g.P("#[derive(Debug, Clone, Serialize, Deserialize)]")
	g.P("pub struct ", rowName, " {")

	for _, field := range msg.Fields {
		if field.Oneof != nil && !field.Oneof.Desc.IsSynthetic() {
			continue
		}
		if isFieldSkipped(field) {
			continue
		}
		// Document ID is excluded from the Row struct (passed separately)
		if isDocumentID(field) {
			continue
		}

		protoName := string(field.Desc.Name())
		rustFieldName := toSnakeCase(protoName)
		fieldType := rustSqliteFieldType(field, opts)

		g.P("    pub ", rustFieldName, ": ", fieldType, ",")
	}

	g.P("}")
	g.P()

	// impl block
	g.P("impl ", rowName, " {")

	// from_row: construct from rusqlite::Row
	g.P("    /// Constructs a ", rowName, " from a rusqlite::Row using named column access.")
	g.P("    pub fn from_row(row: &rusqlite::Row) -> rusqlite::Result<Self> {")
	g.P("        Ok(Self {")

	for _, field := range msg.Fields {
		if field.Oneof != nil && !field.Oneof.Desc.IsSynthetic() {
			continue
		}
		if isFieldSkipped(field) {
			continue
		}
		if isDocumentID(field) {
			continue
		}

		protoName := string(field.Desc.Name())
		rustFieldName := toSnakeCase(protoName)

		g.P("            ", rustFieldName, ": row.get(\"", protoName, "\")?,")
	}

	g.P("        })")
	g.P("    }")
	g.P()

	// to_domain: convert Row to domain type
	g.P("    /// Converts this row to the domain type.")
	if docIDField != nil {
		docIDRustName := toSnakeCase(string(docIDField.Desc.Name()))
		g.P("    pub fn to_domain(&self, ", docIDRustName, ": String) -> ", domainName, " {")
	} else {
		g.P("    pub fn to_domain(&self) -> ", domainName, " {")
	}
	g.P("        ", domainName, " {")

	for _, field := range msg.Fields {
		if field.Oneof != nil && !field.Oneof.Desc.IsSynthetic() {
			continue
		}
		if isFieldSkipped(field) {
			continue
		}

		protoName := string(field.Desc.Name())
		rustFieldName := toSnakeCase(protoName)

		if isDocumentID(field) {
			// Inject from parameter
			g.P("            ", rustFieldName, ",")
			continue
		}

		conversion := rustSqliteToDomainConversion(field, rustFieldName, opts)
		g.P("            ", rustFieldName, ": ", conversion, ",")
	}

	g.P("        }")
	g.P("    }")
	g.P()

	// from_domain: convert domain type to Row
	g.P("    /// Constructs a row from the domain type.")
	g.P("    pub fn from_domain(d: &", domainName, ") -> Self {")
	g.P("        Self {")

	for _, field := range msg.Fields {
		if field.Oneof != nil && !field.Oneof.Desc.IsSynthetic() {
			continue
		}
		if isFieldSkipped(field) {
			continue
		}
		if isDocumentID(field) {
			continue
		}

		protoName := string(field.Desc.Name())
		rustFieldName := toSnakeCase(protoName)

		conversion := rustSqliteFromDomainConversion(field, rustFieldName, opts)
		g.P("            ", rustFieldName, ": ", conversion, ",")
	}

	g.P("        }")
	g.P("    }")

	g.P("}")
	g.P()

	// Nested messages
	for _, nested := range msg.Messages {
		if nested.Desc.IsMapEntry() {
			continue
		}
		generateRustSqliteMessage(g, nested, opts)
	}
}

// rustSqliteFieldType returns the SQLite-appropriate Rust type for a field.
func rustSqliteFieldType(field *protogen.Field, opts *Options) string {
	// Timestamps -> i64 (epoch_ms)
	if isWellKnownTimestamp(field) {
		return "i64"
	}
	// Durations -> i64 (milliseconds)
	if isWellKnownDuration(field) {
		return "i64"
	}

	// Wrapper types -> Option<T> with underlying SQLite type
	if isWellKnownWrapper(field) {
		return rustSqliteWrapperType(field)
	}

	// Nested messages -> String (JSON serialized)
	if field.Desc.Kind() == protoreflect.MessageKind && !field.Desc.IsList() && !field.Desc.IsMap() {
		return "String"
	}

	// Repeated fields -> String (JSON serialized)
	if field.Desc.IsList() {
		return "String"
	}

	// Map fields -> String (JSON serialized)
	if field.Desc.IsMap() {
		return "String"
	}

	// Enum types
	if field.Desc.Kind() == protoreflect.EnumKind {
		if isEnumAsString(field, opts) {
			return "String"
		}
		return "i32"
	}

	// Optional scalars
	if field.Desc.HasOptionalKeyword() {
		return "Option<" + rustSqliteScalarType(field.Desc.Kind()) + ">"
	}

	return rustSqliteScalarType(field.Desc.Kind())
}

// rustSqliteScalarType returns the SQLite-compatible Rust type for a scalar kind.
func rustSqliteScalarType(kind protoreflect.Kind) string {
	switch kind {
	case protoreflect.BoolKind:
		return "bool"
	case protoreflect.Int32Kind, protoreflect.Sint32Kind, protoreflect.Sfixed32Kind:
		return "i32"
	case protoreflect.Int64Kind, protoreflect.Sint64Kind, protoreflect.Sfixed64Kind:
		return "i64"
	case protoreflect.Uint32Kind, protoreflect.Fixed32Kind:
		return "u32"
	case protoreflect.Uint64Kind, protoreflect.Fixed64Kind:
		return "u64"
	case protoreflect.FloatKind:
		return "f32"
	case protoreflect.DoubleKind:
		return "f64"
	case protoreflect.StringKind:
		return "String"
	case protoreflect.BytesKind:
		return "Vec<u8>"
	default:
		return "String"
	}
}

// rustSqliteWrapperType returns the SQLite Option type for a well-known wrapper.
func rustSqliteWrapperType(field *protogen.Field) string {
	switch string(field.Desc.Message().FullName()) {
	case "google.protobuf.StringValue":
		return "Option<String>"
	case "google.protobuf.BoolValue":
		return "Option<bool>"
	case "google.protobuf.Int32Value":
		return "Option<i32>"
	case "google.protobuf.Int64Value":
		return "Option<i64>"
	case "google.protobuf.UInt32Value":
		return "Option<u32>"
	case "google.protobuf.UInt64Value":
		return "Option<u64>"
	case "google.protobuf.FloatValue":
		return "Option<f32>"
	case "google.protobuf.DoubleValue":
		return "Option<f64>"
	case "google.protobuf.BytesValue":
		return "Option<Vec<u8>>"
	default:
		return "Option<String>"
	}
}

// rustSqliteToDomainConversion returns the expression to convert a SQLite row field to a domain field.
func rustSqliteToDomainConversion(field *protogen.Field, fieldName string, opts *Options) string {
	if isWellKnownTimestamp(field) {
		return fmt.Sprintf("epoch_ms_to_datetime(self.%s)", fieldName)
	}
	if isWellKnownDuration(field) {
		return fmt.Sprintf("chrono::Duration::milliseconds(self.%s)", fieldName)
	}
	if isWellKnownWrapper(field) {
		return fmt.Sprintf("self.%s.clone()", fieldName)
	}
	// Nested message: deserialize from JSON string
	if isNestedMessage(field) {
		return fmt.Sprintf("serde_json::from_str(&self.%s).ok().map(Box::new)", fieldName)
	}
	// Repeated message: deserialize from JSON string
	if field.Desc.IsList() {
		return fmt.Sprintf("serde_json::from_str(&self.%s).unwrap_or_default()", fieldName)
	}
	// Map: deserialize from JSON string
	if field.Desc.IsMap() {
		return fmt.Sprintf("serde_json::from_str(&self.%s).unwrap_or_default()", fieldName)
	}
	// Bytes
	if field.Desc.Kind() == protoreflect.BytesKind {
		return fmt.Sprintf("self.%s.clone()", fieldName)
	}
	// String types need clone
	if field.Desc.Kind() == protoreflect.StringKind {
		return fmt.Sprintf("self.%s.clone()", fieldName)
	}
	// Optional scalars
	if field.Desc.HasOptionalKeyword() {
		return fmt.Sprintf("self.%s", fieldName)
	}
	return fmt.Sprintf("self.%s", fieldName)
}

// rustSqliteFromDomainConversion returns the expression to convert a domain field to a SQLite row field.
func rustSqliteFromDomainConversion(field *protogen.Field, fieldName string, opts *Options) string {
	if isWellKnownTimestamp(field) {
		return fmt.Sprintf("datetime_to_epoch_ms(&d.%s)", fieldName)
	}
	if isWellKnownDuration(field) {
		return fmt.Sprintf("d.%s.num_milliseconds()", fieldName)
	}
	if isWellKnownWrapper(field) {
		return fmt.Sprintf("d.%s.clone()", fieldName)
	}
	// Nested message: serialize to JSON string
	if isNestedMessage(field) {
		return fmt.Sprintf("d.%s.as_ref().map(|v| serde_json::to_string(v.as_ref()).unwrap_or_default()).unwrap_or_default()", fieldName)
	}
	// Repeated: serialize to JSON string
	if field.Desc.IsList() {
		return fmt.Sprintf("serde_json::to_string(&d.%s).unwrap_or_default()", fieldName)
	}
	// Map: serialize to JSON string
	if field.Desc.IsMap() {
		return fmt.Sprintf("serde_json::to_string(&d.%s).unwrap_or_default()", fieldName)
	}
	// Bytes
	if field.Desc.Kind() == protoreflect.BytesKind {
		return fmt.Sprintf("d.%s.clone()", fieldName)
	}
	// Strings need clone
	if field.Desc.Kind() == protoreflect.StringKind {
		return fmt.Sprintf("d.%s.clone()", fieldName)
	}
	// Optional scalars
	if field.Desc.HasOptionalKeyword() {
		return fmt.Sprintf("d.%s", fieldName)
	}
	// Enums as string
	if field.Desc.Kind() == protoreflect.EnumKind && isEnumAsString(field, opts) {
		return fmt.Sprintf("d.%s.clone()", fieldName)
	}
	return fmt.Sprintf("d.%s", fieldName)
}

// rustSqliteColumns returns the column names for a message (excluding document_id).
func rustSqliteColumns(msg *protogen.Message) []string {
	var cols []string
	for _, field := range msg.Fields {
		if field.Oneof != nil && !field.Oneof.Desc.IsSynthetic() {
			continue
		}
		if isFieldSkipped(field) {
			continue
		}
		if isDocumentID(field) {
			continue
		}
		cols = append(cols, string(field.Desc.Name()))
	}
	return cols
}

// joinColumns joins column names with a separator.
func joinColumns(cols []string, sep string) string {
	return strings.Join(cols, sep)
}
