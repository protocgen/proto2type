// extractFunctionFields extracts field assignment names (e.g. "id:", "email:")
// from the first occurrence of a function. Returns the field names found.
func extractFunctionFields(src, funcSig string) []string {
	body := extractFunctionBody(src, funcSig)
	if body == "" {
		return nil
	}
	re := regexp.MustCompile(`(?m)^\s+(\w+)[:\s]`)
	matches := re.FindAllStringSubmatch(body, -1)
	var fields []string
	for _, m := range matches {
		fields = append(fields, m[1])
	}
	return fields
}

// extractFunctionBody extracts the body of a function by matching braces.
func extractFunctionBody(src, funcSig string) string {
	idx := strings.Index(src, funcSig)
	if idx < 0 {
		return ""
	}
	// Find opening brace
	braceStart := strings.Index(src[idx:], "{")
	if braceStart < 0 {
		return ""
	}
	start := idx + braceStart
	depth := 0
	for i := start; i < len(src); i++ {
		switch src[i] {
		case '{':
			depth++
		case '}':
			depth--
			if depth == 0 {
				return src[start : i+1]
			}
		}
	}
	return ""
}
